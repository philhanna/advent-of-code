package day8

// ---------------------------------------------------------------------
// Type Definitions
// ---------------------------------------------------------------------
type Vector []byte

// ---------------------------------------------------------------------
// Methods
// ---------------------------------------------------------------------
func (vector Vector) Reverse() Vector {
	n := len(vector)
	for i, j := 0, n-1; i < n/2; i, j = i+1, j-1 {
		vector[i], vector[j] = vector[j], vector[i]
	}
	return vector
}
